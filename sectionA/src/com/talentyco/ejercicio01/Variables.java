public class Variables {
    public static void main(String[] args) {

        // Números enteros
        byte a = 5;
        short b = 200;
        int c= 10000000;
        long d = 100000000000000000L;

        // caracteres
        char e = 97;

        double f = 4.5;
        float g = 4.5f;

        String h = "Bienvenidos a Java en 100 ejercicios.";
        boolean i = true;
        boolean j = 5 > 10;


        System.out.println("a BYTE= " + a);
        System.out.println("b SHORT = " + b);
        System.out.println("c INT = " + c);
        System.out.println("d LONG = " + d);
        System.out.println("e CHAR = " + e);
        System.out.println("f DOUBLE = " + f);
        System.out.println("g FLOAT = " + g);
        System.out.println("h STRING = " + h);
        System.out.println("i BOOLEAN = " + i);
        System.out.println("j BOOLEAN = " + j);

    }
}
